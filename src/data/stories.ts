// Google Web Stories data for ToolStep.
// Each story renders as a standalone AMP <amp-story> page at /stories/[slug]/.
// Images use the text_to_image API with portrait_16_9 size (9:16 vertical).

export interface StoryPage {
  /** Slide identifier (cover, page-1, last, etc.) */
  id: string;
  /** Full image URL (vertical 9:16). */
  image: string;
  /** Optional headline overlaid on the slide. */
  headline?: string;
  /** Optional supporting text. */
  subtext?: string;
  /** Optional CTA button label (only rendered on the last page). */
  ctaLabel?: string;
  /** Optional CTA destination URL. */
  ctaUrl?: string;
}

export interface StoryEntry {
  slug: string;
  title: string;
  category: string;
  color: string;
  description: string;
  /** Vertical poster (9:16). Used for the <amp-story> poster-portrait-src. */
  poster: string;
  /** Square poster (1:1). */
  posterSquare: string;
  /** Landscape poster (16:9). */
  posterLandscape: string;
  /** Publisher logo URL. */
  publisherLogo: string;
  /** Story slides, in order. First = cover, last = CTA. */
  pages: StoryPage[];
  /** Related internal links (other story slugs). */
  relatedStories: string[];
  /** ISO publish date. */
  datePublished: string;
  /** Author slug from authors.ts. */
  authorSlug: string;
}

const PUBLISHER_LOGO = 'https://www.toolstep.top/favicon.svg';

function img(prompt: string, size: string = 'portrait_16_9'): string {
  return `https://trae-api-cn.mchost.guru/api/ide/v1/text_to_image?prompt=${encodeURIComponent(prompt)}&image_size=${size}`;
}

function story(
  slug: string,
  title: string,
  category: string,
  color: string,
  description: string,
  posterPrompt: string,
  slides: Array<{ headline: string; subtext: string; image: string }>,
  ctaLabel: string,
  ctaUrl: string,
  relatedStories: string[],
  authorSlug: string = 'toolstep-team',
): StoryEntry {
  const poster = img(posterPrompt, 'portrait_16_9');
  const posterSquare = img(posterPrompt, 'square');
  const posterLandscape = img(posterPrompt, 'landscape_16_9');

  const pages: StoryPage[] = [];
  pages.push({
    id: 'cover',
    image: poster,
    headline: title,
    subtext: description,
  });
  slides.forEach((s, i) => {
    pages.push({
      id: `page-${i + 1}`,
      image: s.image,
      headline: s.headline,
      subtext: s.subtext,
    });
  });
  pages.push({
    id: 'last',
    image: poster,
    headline: 'Read the full review',
    subtext: title,
    ctaLabel,
    ctaUrl,
  });

  return {
    slug,
    title,
    category,
    color,
    description,
    poster,
    posterSquare,
    posterLandscape,
    publisherLogo: PUBLISHER_LOGO,
    pages,
    relatedStories,
    datePublished: '2026-06-26',
    authorSlug,
  };
}

